/* calculate the first prime (P) */
const uint_64 prime1 = 2017; 

/* calculate the second prime (Q) */
const uint_64 prime2 = 3001;

/* calculate the modulus (M = P*Q) */
const uint_64 modulus = 6053017; // prime1 * prime2;

/* choose the public exponent (E)
 * - must have that E > 1 and E < ( P * Q )
 * - E and (P-1 * Q-1) are coprime
 * - by necessity, E must be odd, but doesn't have to be prime */
const uint_64 publ_exponent = 4283;

/* choose the private exponent (D)
 * - must have that (DE - 1) mod (P-1 * Q-1) == 0
 *   that is: DE-1 is evenly divisible by (P-1 * Q-1) */
const uint_64 priv_exponent = 4056947;

